package IncluirPessoaPOJOPackages;

public class DocumentoAdicional{
	private String dataExpiracao;
	private String tipoDocumento;
	private String orgaoExpedicaoSigla;
	private String numeroDocumento;
	private String dataExpedicao;
	private String ufExpedicao;

	public void setDataExpiracao(String dataExpiracao){
		this.dataExpiracao = dataExpiracao;
	}

	public String getDataExpiracao(){
		return dataExpiracao;
	}

	public void setTipoDocumento(String tipoDocumento){
		this.tipoDocumento = tipoDocumento;
	}

	public String getTipoDocumento(){
		return tipoDocumento;
	}

	public void setOrgaoExpedicaoSigla(String orgaoExpedicaoSigla){
		this.orgaoExpedicaoSigla = orgaoExpedicaoSigla;
	}

	public String getOrgaoExpedicaoSigla(){
		return orgaoExpedicaoSigla;
	}

	public void setNumeroDocumento(String numeroDocumento){
		this.numeroDocumento = numeroDocumento;
	}

	public String getNumeroDocumento(){
		return numeroDocumento;
	}

	public void setDataExpedicao(String dataExpedicao){
		this.dataExpedicao = dataExpedicao;
	}

	public String getDataExpedicao(){
		return dataExpedicao;
	}

	public void setUfExpedicao(String ufExpedicao){
		this.ufExpedicao = ufExpedicao;
	}

	public String getUfExpedicao(){
		return ufExpedicao;
	}
}
